public class comparison_operator {
    public static void main(String[] args) {
        int ka = 10;
        int ma = 5;
        System.out.println(ka > ma);
        System.out.println(ka >= ma);
        System.out.println(ka < ma);
        System.out.println(ka <= ma);
        System.out.println(ka == ma);
        System.out.println(ka != ma);
    }
}
